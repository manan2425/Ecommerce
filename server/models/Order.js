import mongoose from 'mongoose';

const OrderSchema  = new mongoose.Schema({
    userId : String,
    cartItems : [
        {
            productId : String,
            title : String,
            image : String,
            selectedPart: {
                name: String,
                nodeName: String,
                description: String,
                price: Number,
                xPercent: Number,
                yPercent: Number,
                thumbnail: String
            },
            price : String,
            salePrice : String,
            quantity : Number
        }
    ],
    addressInfo : {
        addressId : String,
        address : String,
        city : String,
        pincode : String,
        phone : String,
        notes : String,
    },
    orderStatus : String,
    paymentMethod : String,
    paymentStatus : String,
    totalAmount : Number,
    orderDate : Date,
    orderUpdateDate : Date,
    paymentId : String,
    payerId : String,
});

const Order = mongoose.model('Order', OrderSchema);
export default Order;